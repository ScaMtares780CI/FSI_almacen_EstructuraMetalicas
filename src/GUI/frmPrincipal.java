    private void cerrarSesion() {
        int seleccion = JOptionPane.showOptionDialog(
                this,
                "Seleccione opcion",
                "Selector de opciones",
                JOptionPane.YES_NO_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null, // null para icono por defecto.
                new Object[]{"Cerrar sesi√≥n", "Salir"}, // null para YES, NO y CANCEL
                "Cerrar sesi√≥n");
        switch (seleccion) {
            case 0: //Cerrar sesi√≥n
                if (frmGestionarClientes != null) {
                    frmGestionarClientes.dispose();
                }
                if (frmNuevoCliente != null) {
                    frmNuevoCliente.dispose();
                }
                if (frmNuevoPedido != null) {
                    frmNuevoPedido.dispose();
                }
                if (frmGestionarPedidos != null) {
                    frmGestionarPedidos.dispose();
                }
                if (frmCambiarFechaRecojo != null) {
                    frmCambiarFechaRecojo.dispose();
                }
                if (frmListarPedidos != null) {
                    frmListarPedidos.dispose();
                }
                this.dispose();
                new frmLogin().setVisible(true);
                break;
            case 1:
                System.exit(0);
                break;
        }
    }
