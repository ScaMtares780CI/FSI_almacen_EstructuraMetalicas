    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Debe ingresar una fecha correcta.");
        } else {
            Pedido P = daoPedido.instancia().obtener(indice);
            P.setEstado(Pedido.Estado.EN_PROCESO);
            P.setFechaEntrega(jDateChooser1.getDate());
            daoPedido.instancia().modificar(indice, P);
            JOptionPane.showMessageDialog(this, "Fecha modificada con Ã©xito.");
            if (frmPrincipal.frmGestionarPedidos != null) {
                frmPrincipal.frmGestionarPedidos.listarPedidos();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

